# Input for the Radius 
what_is_radian = int(input('What is the radius of your Circle? '))

# Calculating the area of the circle
area_of_circle = 3.14159265359 * what_is_radian ** 2

# Printing the Results
print(f"The area of the circle with the radius of {what_is_radian} is {round(area_of_circle, 2)}")